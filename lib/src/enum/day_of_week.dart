enum DayOfWeek {
  /// Sunday - 0
  sunday(0),

  /// Monday - 1
  monday(1),

  /// Tuesday - 2
  tuesday(2),

  /// Wednesday - 3
  wednesday(3),

  /// Thursday - 4
  thursday(4),

  /// Friday - 5
  friday(5),

  /// Saturday - 6
  saturday(6);

  const DayOfWeek(this.value);
  final int value;
}
